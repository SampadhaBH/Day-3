//write a java script to uppercase the the alphabets
// Function to convert the alphabet to uppercase
function uppercaseAlphabet() {
    const alphabet = 'abcdefghijklmnopqrstuvwxyz'; // Define the alphabet
    const uppercaseAlphabet = alphabet.toUpperCase(); // Convert to uppercase
    console.log("Uppercase alphabet is:", uppercaseAlphabet); // Print the result
}

// Call the function to convert the alphabet to uppercase
uppercaseAlphabet();