//write a java script to lowercase the the alphabets

// Function to convert the alphabet to lowercase
function lowercaseAlphabet() {
    const alphabet = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ'; // Define the alphabet in uppercase
    const lowercaseAlphabet = alphabet.toLowerCase(); // Convert to lowercase
    console.log("Lowercase alphabet is:", lowercaseAlphabet); // Print the result
}

// Call the function to convert the alphabet to lowercase
lowercaseAlphabet();