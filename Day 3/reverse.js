// Function to reverse the alphabet
function reverseAlphabet() {
    const alphabet = 'abcdefghijklmnopqrstuvwxyz'; // Define the alphabet
    const reversedAlphabet = alphabet.split('').reverse().join(''); // Reverse the alphabet
    console.log("Reversed alphabet is:", reversedAlphabet); // Print the result
}

// Call the function to reverse the alphabet
reverseAlphabet();